export const themes = [main, hellokitty, kuromi]

export function applyTheme(newTheme: string): void {
  const theme = themes.find((t) => t.name.toLowerCase() === newTheme?.toLowerCase()) || main

  const root = document.documentElement
  Object.entries(theme).forEach(([key, value]: [string, string | null]) => {
    if (value === null) return
    root.style.setProperty(`--${key.replace(/([A-Z])/g, '-$1').toLowerCase()}`, value)
  })

  updatePreset({
    semantic: {
      primary: {
        0: theme.primary,
        50: theme.primary,
        100: theme.primary,
        200: theme.primary,
        300: theme.primary,
        400: theme.primary,
        500: theme.primary,
        600: theme.primary,
        700: theme.primary,
        800: theme.primary,
        900: theme.primary,
        950: theme.primary
      },
      colorScheme: {
        light: {
          surface: {
            0: theme.bgLight,
            50: theme.bgLight,
            100: theme.textSecondary,
            200: theme.textSecondary,
            300: theme.textSecondary,
            400: theme.textSecondary,
            500: theme.textSecondary,
            600: theme.textSecondary,
            700: theme.textSecondary,
            800: theme.bgLight,
            900: theme.bgLight,
            950: theme.bgLight
          },
          semantic: {
            highlight: {
              background: theme.primary,
              color: theme.textPrimary
            }
          }
        }
      }
    }
  })
}
